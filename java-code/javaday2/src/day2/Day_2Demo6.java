public class Day_2Demo6 {
    public static void main(String[] args) {
        //1. ==
        int a = 10;
        int b = 10;
        int c = 20;
        System.out.println(a == b);//true
        System.out.println(a == c);//false

        //2. !=
        System.out.println(a != b);//false
        System.out.println(a != c);//true
    }
}
